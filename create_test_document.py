#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
创建测试文档脚本
用于生成一个包含四个部分的测试视频脚本
"""

from docx import Document


def create_test_document():
    """创建测试文档"""

    doc = Document()

    # 第一部分：引入
    doc.add_paragraph("第一部分：引入")
    doc.add_paragraph("""
大家好！今天我们要学习一个非常有趣的数学知识（播放开场动画）。
你们知道吗？数学其实就在我们的生活中【展示生活场景图片】。
让我们一起来探索吧！(背景音乐渐起)
这节课我们会学到很多好玩的内容，请小朋友们认真听讲哦。
""".strip())

    # 第二部分：知识点讲解
    doc.add_paragraph("\n第二部分：知识点讲解")

    doc.add_paragraph("知识点1：加法的基本概念")
    doc.add_paragraph("""
首先，我们来看看什么是加法（展示PPT第1页）。
加法就是把两个数字合在一起【动画演示：2个苹果+3个苹果】。
比如说，小明有2个苹果，小红给了他3个苹果(展示动画)，
那么小明现在一共有几个苹果呢？对了！是5个苹果「点赞动画」。
这就是2加3等于5，我们可以写成：2 + 3 = 5（板书展示）。
""".strip())

    doc.add_paragraph("\n知识点2：加法的运算方法")
    doc.add_paragraph("""
接下来，让我们学习如何计算加法［展示计算步骤］。
我们可以用手指来帮助计算｛演示手指计数｝。
比如计算 4 + 3，我们先伸出4根手指(动作演示)，
然后再伸出3根手指（慢动作展示），
数一数一共有几根？没错！是7根『鼓掌音效』。
这个方法是不是很简单呢【竖起大拇指】？
""".strip())

    doc.add_paragraph("\n知识点3：加法练习")
    doc.add_paragraph("""
现在我们来做一些简单的练习题（展示练习题PPT）。
请小朋友们跟着老师一起算一算：
1 + 1 = ？（停顿3秒，等待思考）
5 + 2 = ？【显示思考泡泡】
3 + 4 = ？(播放思考音乐)
大家都算出来了吗「展示答案动画」？
如果还不太明白也没关系，我们可以多练习几次［鼓励的表情］。
""".strip())

    # 第三部分：综合练习
    doc.add_paragraph("\n第三部分：综合练习")
    doc.add_paragraph("""
现在让我们通过一些实际问题来巩固今天学到的知识（切换到练习场景）。

第一题：小猫咪的问题【展示小猫图片】
院子里有3只小猫(动画出现3只猫)，
又来了4只小猫（动画再出现4只猫），
现在一共有几只小猫呢？
请大家在心里算一算「显示计时器30秒」！

第二题：水果摊的问题［切换到水果摊场景］
摊上有6个橙子｛展示橙子图片｝，
老板又拿来2个橙子(动画效果)，
一共有多少个橙子？
想想看，答案是什么【思考表情包】？

做得真棒！让我们一起来看看答案（播放揭晓音效）。
第一题答案是7只小猫「正确答案动画」，
第二题答案是8个橙子［星星特效］。
""".strip())

    # 第四部分：总结
    doc.add_paragraph("\n第四部分：总结")
    doc.add_paragraph("""
今天我们学习了加法的知识（回顾PPT）。
我们知道了加法就是把数字合在一起【复习动画】。
我们还学会了用手指来帮助计算(演示回顾)。
希望小朋友们回家后多多练习「展示作业」，
这样就能算得又快又准了［竖大拇指］！

下节课我们会学习减法，同样很有趣哦｛预告下节课内容｝。
今天的课程就到这里(挥手再见动画)，
小朋友们再见【片尾音乐】！
""".strip())

    # 保存文档
    output_file = "测试视频脚本.docx"
    doc.save(output_file)
    print(f"✅ 测试文档已创建: {output_file}")

    # 打印统计信息
    print("\n文档结构:")
    print("- 第一部分：引入")
    print("- 第二部分：知识点讲解（包含3个知识点）")
    print("- 第三部分：综合练习")
    print("- 第四部分：总结")
    print("\n包含的括号类型:")
    print("- ()、（）、[]、【】、「」、『』、{}、｛｝")


if __name__ == '__main__':
    create_test_document()
